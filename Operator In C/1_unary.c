


#include<stdio.h>

int main(){

    // unary operator 

    int num1 = 10;

    printf("\npre increment operator: %d", ++num1);

    printf("\npre decrement operator: %d",--num1);

    // post increment

    printf("\npost increment operator: %d", num1++);

    printf("\npost increment operator: %d ", num1);

    // post decrement 

    printf("\npost decrement operator: %d", num1--);

    printf("\npost decrement operator: %d ", num1);


     

    return 0;
}